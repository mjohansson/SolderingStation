void ssd1322_init(void)
{
    // Set BS[1:0] to select bus interface cfg
    // 00 = 4-line SPI (D0 = SCLK; D1 = MOSI; D2 = N/C; Dx = gnd)
    // 01 = 3-line SPI
    // 10 = 8b 8080 parallel
    // 11 = 8b 6800 parallel
// NOTE: BS is hardwired to 00 on SolderStation Frontpanel-inner!
//    HAL_GPIO_WritePin(DISP_BS1_GPIO_Port, DISP_BS1_Pin, GPIO_PIN_RESET);
//    HAL_GPIO_WritePin(DISP_BS0_GPIO_Port, DISP_BS0_Pin, GPIO_PIN_RESET);

    // Release display CSn and DCn
    HAL_GPIO_WritePin(DISP_DCn_GPIO_Port, DISP_DCn_Pin, GPIO_PIN_SET);
    HAL_GPIO_WritePin(DISP_CSn_GPIO_Port, DISP_CSn_Pin, GPIO_PIN_SET);
    // assert reset
    HAL_GPIO_WritePin(SYS_RSTn_GPIO_Port, SYS_RSTn_Pin, GPIO_PIN_RESET);
    HAL_Delay(1);
    // release reset
    HAL_GPIO_WritePin(SYS_RSTn_GPIO_Port, SYS_RSTn_Pin, GPIO_PIN_SET);
    HAL_Delay(1);

    // init seq.
    HAL_GPIO_WritePin(DISP_CSn_GPIO_Port, DISP_CSn_Pin, GPIO_PIN_RESET);
    __NOP();
    __NOP();

    set_command_unlock();
    set_display_off();
    set_column_address(0x1c, 0x5b);  // col0 = seg112 = addr 0x1c -> col255 = seg367 = addr 0x5b
    set_row_address(0x00, 0x3f);
    set_display_clock(0x91);   // 80 FPS
    set_mux_ratio(0x3f);       // 1/64
    set_display_offset(0x00);
    set_start_line(0x00);
    set_remap_format(0x14);    // H+, C0 = SEG0, Enable nibble remap, scan COM63..COM0,
                               //  (Implicit B = 0x11 ; No COM split, dual COM line mode)
    set_gpio(0x00);
    set_function_selection(0x01);
    set_display_enh_a(0xa0, 0xfd);     // Enable external VSL
    set_contrast(112);
    set_master_contrast(0x0f);
    set_linear_grayscale();
    set_phase_length(0xe2);            // Phase 1=5 clks, phase 2=14 clks
    set_display_enh_b(0x20);           // Enhance driving scheme capability
    set_precharge_voltage(0x1f);       // 0.60*VCC
    set_precharge_period(0x08);        // 2nd precharge period = 8 clks
    set_vcomh(0x07);                   // COM deselect voltage = 0.86*VCC
    set_display_mode(DISPLAY_NORMAL);  // Display mode = Normal
    exit_partial_display();
    HAL_GPIO_WritePin(DISP_CSn_GPIO_Port, DISP_CSn_Pin, GPIO_PIN_SET);
    HAL_Delay(100);

    HAL_GPIO_WritePin(DISP_CSn_GPIO_Port, DISP_CSn_Pin, GPIO_PIN_RESET);
    set_display_on();
    HAL_GPIO_WritePin(DISP_CSn_GPIO_Port, DISP_CSn_Pin, GPIO_PIN_SET);

}


void ssd1322_uninit(void)
{
    HAL_GPIO_WritePin(DISP_CSn_GPIO_Port, DISP_CSn_Pin, GPIO_PIN_RESET);
    set_display_off();
    HAL_GPIO_WritePin(DISP_CSn_GPIO_Port, DISP_CSn_Pin, GPIO_PIN_SET);
    HAL_Delay(10);

    // TODO: disable HV boost..?
    // HAL_Delay(100);

    // Release display CSn and DCn
    HAL_GPIO_WritePin(DISP_DCn_GPIO_Port, DISP_DCn_Pin, GPIO_PIN_SET);
    HAL_GPIO_WritePin(DISP_CSn_GPIO_Port, DISP_CSn_Pin, GPIO_PIN_SET);
    // Assert reset; then set all signals "off"
    HAL_Delay(1);
    HAL_GPIO_WritePin(SYS_RSTn_GPIO_Port, SYS_RSTn_Pin, GPIO_PIN_RESET);
    HAL_Delay(1);
    HAL_GPIO_WritePin(DISP_DCn_GPIO_Port, DISP_DCn_Pin, GPIO_PIN_RESET);
    HAL_GPIO_WritePin(DISP_CSn_GPIO_Port, DISP_CSn_Pin, GPIO_PIN_RESET);
}

