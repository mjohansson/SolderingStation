static void set_grayscale_table(uint8_t *data, uint32_t length)
{
    if (data == NULL || length != 15) {
        return;
    }

    // Set the table periods GS1..GS15
    ssd1322_cmd(SET_GRAYSCALE_TABLE);
    ssd1322_dataN(data, length);
    ssd1322_cmd0(ENABLE_GRAYSCALE_TABLE);
}


